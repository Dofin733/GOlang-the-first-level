package main

import (
	"fmt"
	"math"
)

func main() {
	var s float64
	fmt.Print("Введите площадь круга: ") // просим пользователя ввести площадь
	fmt.Scanln(&s)

	r := math.Sqrt(s / math.Pi) // вычесление радиуса окружности через площыдь
	d := r * 2 // вычисление диаметра окружности через площадь
	l := math.Pi * d // вычисляем длину окружности

    fmt.Printf ( "Диаметр окружности равен: %f\n", d )
    fmt.Printf( "Длина окружности равна: %f\n", l )
}