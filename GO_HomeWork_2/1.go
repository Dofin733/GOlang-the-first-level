package main

import (
"fmt"
)

//вычисление площади прямоугольника
func main() {
	var a, b float32

	fmt.Print("Введите длинну прямоугольника: ") // просим ввести длину
	fmt.Scanln(&a)

	fmt.Print("Введите ширину прямоугольника: ") // просим ввести ширину
	fmt.Scanln(&b)

	fmt.Printf("Площадь прямоугольника равна: %f\n", a*b) // выводим результат умножения длины на ширину

}